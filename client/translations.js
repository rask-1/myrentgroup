// Current language
let currentLang = 'ru';

// Set language
function setLanguage(lang) {
  if (!translations[lang]) return;
  
  currentLang = lang;
  localStorage.setItem('lang', lang);
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const value = translations[lang][key];
    
    if (value) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = value;
      } else {
        element.innerHTML = value;
      }
    }
  });
  
  // Update select options
  updateSelectOptions(lang);
  
  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  
  // Update html lang attribute
  document.documentElement.lang = lang;
}

// Update select options
function updateSelectOptions(lang) {
  // Budget select
  const budgetSelects = document.querySelectorAll('select[data-i18n-budget]');
  budgetSelects.forEach(select => {
    const options = select.querySelectorAll('option');
    if(options[0]) options[0].textContent = translations[lang].budgetSelect;
    if(options[1]) options[1].textContent = translations[lang].budget50_100;
    if(options[2]) options[2].textContent = translations[lang].budget100_200;
    if(options[3]) options[3].textContent = translations[lang].budget200_300;
    if(options[4]) options[4].textContent = translations[lang].budget300_500;
    if(options[5]) options[5].textContent = translations[lang].budget500plus;
  });
  
  // Goal select
  const goalSelects = document.querySelectorAll('select[data-i18n-goal]');
  goalSelects.forEach(select => {
    const options = select.querySelectorAll('option');
    if(options[0]) options[0].textContent = translations[lang].goalSelect;
    if(options[1]) options[1].textContent = translations[lang].goalInvestment;
    if(options[2]) options[2].textContent = translations[lang].goalLiving;
    if(options[3]) options[3].textContent = translations[lang].goalRent;
    if(options[4]) options[4].textContent = translations[lang].goalVacation;
  });
  
  // Country select
  const countrySelects = document.querySelectorAll('select.country-code');
  countrySelects.forEach(select => {
    const options = select.querySelectorAll('option');
    if(options[0]) options[0].textContent = translations[lang].countrySelect;
    
    // Update country options
    const countryKeys = [
      'countryRU', 'countryBY', 'countryUA', 'countryKZ', 'countryUZ',
      'countryAZ', 'countryGE', 'countryAM', 'countryMD', 'countryKG',
      'countryTJ', 'countryTM', 'countryTR', 'countryDE', 'countryFR',
      'countryIT', 'countryES', 'countryPL', 'countryGB', 'countryNL',
      'countryBE', 'countryCH', 'countryAT', 'countrySE', 'countryNO',
      'countryDK', 'countryFI', 'countryPT', 'countryGR', 'countryCZ',
      'countryRO', 'countryBG', 'countryHU', 'countryCN', 'countryIL',
      'countryIN', 'countryJP', 'countryKR', 'countrySG', 'countryTH',
      'countryVN', 'countryMY', 'countryPH', 'countryID', 'countryPK',
      'countryAE', 'countrySA', 'countryQA', 'countryKW', 'countryBH',
      'countryOM', 'countryUS', 'countryCA', 'countryMX', 'countryBR',
      'countryAR', 'countryCL', 'countryCO', 'countryPE', 'countryZA',
      'countryEG', 'countryAU', 'countryNZ'
    ];
    
    countryKeys.forEach((key, index) => {
      if(options[index + 1] && translations[lang][key]) {
        options[index + 1].textContent = translations[lang][key];
      }
    });
  });
}

// Initialize language
function initLanguage() {
  const savedLang = localStorage.getItem('lang') || 'ru';
  setLanguage(savedLang);
}

// Make functions global
window.setLanguage = setLanguage;
window.initLanguage = initLanguage;